"""Template for the two classes hosts should customize for each competition."""

import abc
import atexit
import os
import time
import warnings
from pathlib import Path
from types import FunctionType, MethodType
from typing import Any, Generator

import pandas as pd
import polars as pl

import kaggle_evaluation.core.base_gateway
import kaggle_evaluation.core.relay

_initial_import_time = time.time()
_issued_startup_time_warning = False


class Gateway(kaggle_evaluation.core.base_gateway.BaseGateway, abc.ABC):
    """
    Template to start with when writing a new gateway.
    In most cases, hosts should only need to edit the three abstract methods below.

    There are two main methods for sending data to the inference_server hosts should understand:
    - Small datasets: use `self.predict`, which gets called by default.
    Competitors will receive the data passed to self.predict as Python objects in memory. This is
    just a wrapper for self.client.send(); you can write additional wrappers if necessary.
    - Large datasets: it's much faster to send data via self.share_files, which is equivalent to making
    files available via symlink. See base_gateway.BaseGateway.share_files for the full details.
    """

    @abc.abstractmethod
    def generate_data_batches(self) -> Generator:
        """Used by the default implementation of `get_all_predictions` so we can
        ensure `competition_agnostic_validation` is run every time `predict` is called.

        This method must yield both the batch of data to be sent to `predict` and a series
        of row IDs to be used for validation and the submission file. For example:
            return (test_feature_batch, test_metadata_batch), row_id_batch
        """
        raise NotImplementedError

    @abc.abstractmethod
    def competition_specific_validation(
        self,
        prediction_batch: Any,
        row_ids: pl.DataFrame | pl.Series | pd.DataFrame | pd.Series,
        data_batch: Any,
    ) -> None:
        """Competition specific checks should be added here. Typically you'll want to confirm the predictions are a valid data type at a minimum.
        Args:
            - prediction_batch: The user's predictions.
            - row IDs: The associated row IDs generated by the gateway.
            - data_batch: Data originally provided to the user.
        """
        raise NotImplementedError


class GameGateway(kaggle_evaluation.core.base_gateway.BaseGateway, abc.ABC):
    """Template for game-based competitions.

    Unlike data science competitions which iterate over data batches calling predict(),
    games run a game loop and produce final scores. This class provides a cleaner
    interface that doesn't require implementing unused data science methods.

    Subclasses must implement:
    - run_game(): Execute the game and return agent scores
    """

    def __init__(
        self,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        server_names: list[str] | None = None,
        server_ports: dict[str, int] | None = None,
        allowed_modules: list[str] | None = None,
    ):
        """Initialize the game gateway.

        Args:
            competition_data_folder: Optional base directory for game data/configuration files.
            file_share_dir: Directory for sharing files with inference server.
            server_names: List of inference server names for multi-agent games.
            server_ports: Optional dict mapping server names to specific ports.
            allowed_modules: Module names allowed for Pydantic/dataclass deserialization.
        """
        super().__init__(
            competition_data_folder=competition_data_folder,
            file_share_dir=file_share_dir,
            server_names=server_names,
            server_ports=server_ports,
            allowed_modules=allowed_modules,
        )
        self.row_id_column_name = 'agent_id'
        self.target_column_name = 'score'

    @abc.abstractmethod
    def run_game(self) -> dict[str, float | int]:
        """Run the game and return agent scores.

        This is the main method game gateways must implement. It should:
        1. Initialize the game environment
        2. Run the game loop, communicating with agents via self.client
        3. Return final scores for each agent

        Returns:
            Dict mapping agent IDs (str) to their final scores.
        """
        raise NotImplementedError

    def run(self) -> None:
        """Execute the game and write results.

        Orchestrates the full game flow:
        1. Run the game
        2. Write submission file with scores
        3. Handle errors and cleanup
        """
        error = None
        try:
            results = self.run_game()
            self._write_game_results(results)
        except kaggle_evaluation.core.base_gateway.GatewayRuntimeError as gre:
            error = gre
        except Exception as e:
            import traceback

            error_str = ''.join(traceback.format_exception(e))
            error = kaggle_evaluation.core.base_gateway.GatewayRuntimeError(
                kaggle_evaluation.core.base_gateway.GatewayRuntimeErrorType.GATEWAY_RAISED_EXCEPTION,
                error_str,
            )

        for client in self.clients.values():
            client.close()
        if self.server:
            self.server.stop(0)

        if kaggle_evaluation.core.base_gateway.IS_RERUN:
            self.write_result(error)
        elif error:
            raise error

    def _write_game_results(self, results: dict[str, float | int]) -> None:
        """Convert game results to submission format and write."""
        row_id_col = self.row_id_column_name or 'agent_id'
        target_col = self.target_column_name or 'score'
        row_ids = pl.DataFrame(list(results.keys()), schema={row_id_col: pl.Utf8})
        predictions = row_ids[row_id_col].replace_strict(results).rename(target_col)
        self.write_submission([predictions], [row_ids])

    # These methods are not used by games but are defined in BaseGateway.
    # We provide no-op implementations to avoid forcing subclasses to implement them.
    def generate_data_batches(self) -> Any:
        """Not used by game gateways."""
        raise NotImplementedError('Game gateways do not use generate_data_batches')

    def competition_specific_validation(
        self,
        prediction_batch: Any,
        row_ids: pl.DataFrame | pl.Series | pd.DataFrame | pd.Series,
        data_batch: Any,
    ) -> None:
        """Not used by game gateways - game engines validate moves directly."""
        pass


def _should_skip_atexit_warnings():
    """The warnings are suppressed when KAGGLE_KERNEL_RUN_TYPE is Interactive because in a live notebook session,
    users may define their server class in one cell and not call run() until a later cell;
    a warning on kernel restart would be a false positive."""
    return os.getenv('KAGGLE_KERNEL_RUN_TYPE') == 'Interactive'


def _warn_if_never_instantiated(cls):
    if not cls._instantiated:
        warnings.warn(
            f'{cls.__name__} was never used. Did you forget to call {cls.__name__}(...).run()?',
            category=RuntimeWarning,
            stacklevel=1,
        )


class InferenceServer(abc.ABC):
    """
    Base class for competition participants to inherit from when writing their submission. In most cases, users should
    only need to implement a `predict` function or other endpoints to pass to this class's constructor, and hosts will
    provide a mock Gateway for testing.
    """

    def __init_subclass__(cls, **kwargs):
        super().__init_subclass__(**kwargs)
        cls._instantiated = False
        if not _should_skip_atexit_warnings():
            atexit.register(_warn_if_never_instantiated, cls)

    def __init__(
        self,
        *endpoint_listeners: FunctionType | MethodType,
        allowed_modules: list[str] | None = None,
    ):
        """Create an inference server with one or more endpoint functions.

        Args:
            *endpoint_listeners: Functions the gateway can call. The first is typically your predict function.
            allowed_modules: Module names allowed for Pydantic/dataclass deserialization. Only needed if the
                competition sends structured types (enums, dataclasses, Pydantic models).
        """
        # Setting allowed modules must happen before sending any data to the gateway.
        kaggle_evaluation.core.relay.set_allowed_modules(allowed_modules)

        self.__class__._instantiated = True
        self.server, self.port = kaggle_evaluation.core.relay.define_server(*endpoint_listeners)
        self.client = None  # The inference_server can have a client but it isn't typically necessary.
        self._issued_startup_time_warning = False
        self._started = False
        self._startup_limit_seconds = kaggle_evaluation.core.relay.STARTUP_LIMIT_SECONDS
        if not _should_skip_atexit_warnings():
            atexit.register(self._warn_if_never_started)

    def _warn_if_never_started(self) -> None:
        if not self._started:
            warnings.warn(
                f'{self.__class__.__name__} was created but run() was never called. This will cause your submission to fail on the hidden test set.',
                category=RuntimeWarning,
                stacklevel=1,
            )

    def serve(self) -> None:
        """Start the gRPC server. During a competition rerun, blocks until the gateway terminates it."""
        self._started = True
        self.server.start()
        if os.getenv('KAGGLE_IS_COMPETITION_RERUN') is not None:
            self.server.wait_for_termination()  # This will block all other code

    def run(
        self,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> None:
        """Start the server. During a competition rerun, blocks until the gateway terminates the server.
        Otherwise, runs a local copy of the gateway for testing."""
        self._started = True
        if os.getenv('KAGGLE_IS_COMPETITION_RERUN') is not None:
            self.serve()
        else:
            self.run_local_gateway(competition_data_folder, file_share_dir, *args, **kwargs)

    def _get_gateway_for_test(
        self,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> kaggle_evaluation.core.base_gateway.BaseGateway:
        """Return a gateway able to load data for unit tests.

        Auto-discovers the gateway class. Override if the gateway needs custom
        construction (e.g. non-default data_paths, extra server names).
        """
        from kaggle_evaluation.core.gateway_discovery import find_gateway_class

        gateway_cls = find_gateway_class()
        return gateway_cls(competition_data_folder=competition_data_folder, file_share_dir=file_share_dir, *args, **kwargs)

    def run_local_gateway(
        self,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> None:
        """Construct a copy of the gateway that uses local file paths."""
        self._started = True
        global _issued_startup_time_warning
        script_elapsed_seconds = time.time() - _initial_import_time
        if script_elapsed_seconds > self._startup_limit_seconds and not _issued_startup_time_warning:
            warnings.warn(
                f"""{int(script_elapsed_seconds)} seconds elapsed before server startup.
                This exceeds the startup time limit of {int(self._startup_limit_seconds)} seconds that the gateway will enforce
                during the rerun on the hidden test set. Start the server before performing any time consuming steps.""",
                category=RuntimeWarning,
            )
            _issued_startup_time_warning = True

        self.server.start()
        try:
            self.gateway = self._get_gateway_for_test(competition_data_folder, file_share_dir, *args, **kwargs)
            self.gateway.clients = {self.gateway.server_names[0]: kaggle_evaluation.core.relay.Client(port=self.port)}
            self.gateway.run()
        except Exception as err:
            raise err from None
        finally:
            self.server.stop(0)


class GameInferenceServer(abc.ABC):
    """Base class for game competition participants.

    For multiplayer games, each agent runs in a separate container with its own relay connection.
    This class provides a simpler interface for game agents compared to InferenceServer.

    Subclasses should:
    1. Implement an agent class with a process_turn method
    2. Pass the agent's process_turn method to the constructor
    """

    def __init_subclass__(cls, **kwargs):
        super().__init_subclass__(**kwargs)
        cls._instantiated = False
        if not _should_skip_atexit_warnings():
            atexit.register(_warn_if_never_instantiated, cls)

    def __init__(
        self,
        *endpoint_listeners: FunctionType | MethodType,
        allowed_modules: list[str] | None = None,
    ):
        """Create a game inference server with one or more endpoint functions.

        Args:
            *endpoint_listeners: Functions the gateway can call. Typically your agent's process_turn method.
            allowed_modules: Module names allowed for Pydantic/dataclass deserialization. Only needed if the
                competition sends structured types (enums, dataclasses, Pydantic models).
        """
        kaggle_evaluation.core.relay.set_allowed_modules(allowed_modules)

        self.__class__._instantiated = True
        self.server, self.port = kaggle_evaluation.core.relay.define_server(*endpoint_listeners)
        self._started = False
        self._startup_limit_seconds = kaggle_evaluation.core.relay.STARTUP_LIMIT_SECONDS
        if not _should_skip_atexit_warnings():
            atexit.register(self._warn_if_never_started)

    def _warn_if_never_started(self) -> None:
        if not self._started:
            warnings.warn(
                f'{self.__class__.__name__} was created but run() was never called. This will cause your submission to fail on the hidden test set.',
                category=RuntimeWarning,
                stacklevel=1,
            )

    def serve(self) -> None:
        """Start the server and wait for termination during competition reruns."""
        self._started = True
        self.server.start()
        if os.getenv('KAGGLE_IS_COMPETITION_RERUN') is not None:
            self.server.wait_for_termination()

    def run(
        self,
        opponents: list[Any] | None = None,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> None:
        """Start the server. During a competition rerun, blocks until the gateway terminates the server.
        Otherwise, runs a local game for testing."""
        self._started = True
        if os.getenv('KAGGLE_IS_COMPETITION_RERUN') is not None:
            self.serve()
        else:
            if opponents is None:
                opponents = []
            self.run_local_game(opponents, competition_data_folder, file_share_dir, *args, **kwargs)

    def _get_gateway_for_test(
        self,
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> kaggle_evaluation.core.base_gateway.BaseGateway:
        """Return a GameGateway configured for local testing.

        Auto-discovers the gateway class. Override if the game gateway needs
        custom construction (e.g. explicit server_names for multi-agent games).
        """
        from kaggle_evaluation.core.gateway_discovery import find_gateway_class

        gateway_cls = find_gateway_class()
        return gateway_cls(competition_data_folder=competition_data_folder, file_share_dir=file_share_dir, *args, **kwargs)

    def run_local_game(
        self,
        opponents: list[Any],
        competition_data_folder: str | Path | None = None,
        file_share_dir: str | None = None,
        *args,
        **kwargs,
    ) -> None:
        """Run a local game with the main agent playing against opponent agents.

        For an N-player game, provide N-1 opponents. The main agent (self) is always included.

        Args:
            opponents: List of agent instances to play against. Each must have a process_turn method.
            competition_data_folder: Optional base directory for game data/configuration files.
            file_share_dir: Directory for sharing files with inference server.
            *args, **kwargs: Additional arguments passed to _get_gateway_for_test.
        """
        self._started = True
        global _issued_startup_time_warning
        script_elapsed_seconds = time.time() - _initial_import_time
        if script_elapsed_seconds > self._startup_limit_seconds and not _issued_startup_time_warning:
            warnings.warn(
                f"""{int(script_elapsed_seconds)} seconds elapsed before server startup.
                This exceeds the startup time limit of {int(self._startup_limit_seconds)} seconds that the gateway will enforce
                during the rerun on the hidden test set. Start the server before performing any time consuming steps.""",
                category=RuntimeWarning,
            )
            _issued_startup_time_warning = True

        # Start the main agent's server
        servers = [self.server]
        self.server.start()

        # Start a server for each opponent agent
        opponent_ports: list[int] = []
        for opponent in opponents:
            if not hasattr(opponent, 'process_turn'):
                raise ValueError(f'Opponent {opponent.__class__.__name__} must have a process_turn method')
            server, port = kaggle_evaluation.core.relay.define_server(opponent.process_turn)
            server.start()
            servers.append(server)
            opponent_ports.append(port)

        try:
            self.gateway = self._get_gateway_for_test(competition_data_folder, file_share_dir, *args, **kwargs)

            # Build clients dict: main agent first, then opponents
            # Server names should match the number of players
            if len(self.gateway.server_names) != len(opponents) + 1:
                raise ValueError(f'Gateway expects {len(self.gateway.server_names)} agents but got 1 main agent + {len(opponents)} opponents')

            self.gateway.clients = {}
            self.gateway.clients[self.gateway.server_names[0]] = kaggle_evaluation.core.relay.Client(port=self.port)
            for i, port in enumerate(opponent_ports):
                self.gateway.clients[self.gateway.server_names[i + 1]] = kaggle_evaluation.core.relay.Client(port=port)

            self.gateway.run()
        except Exception as err:
            raise err from None
        finally:
            for server in servers:
                server.stop(0)
